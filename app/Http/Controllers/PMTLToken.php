    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $files = Storage::files('/');
        $tasks = [];
        foreach ($files as $val) {
            $fname = explode(".", $val);
            if (count($fname) == 3 && $fname[2] == 'json' && $fname[0] == $request['listName']) {
                $contents = Storage::get($val);
                array_push($tasks, json_decode($contents));
            }
        }
/*
        $response = new \Illuminate\Http\Response($tasks);

        $response->withCookie(cookie()->forever('listName', $request['listName']));

        return $response;
*/
        return response()->json(compact('tasks'), 201)->withCookie(cookie()->forever('listName', $request['listName']));
    }

